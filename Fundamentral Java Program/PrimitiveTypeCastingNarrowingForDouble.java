import java.util.Scanner;
public class PrimitiveTypeCastingNarrowingForDouble{
	public static void main(String [] args){
		double d =87565.5587;
		float f = (float)d;
		long l = (long)d;
		int i = (int)d;
		short s = (short)d;
		byte b =(byte) d;
		char c = (char) d;
	System.out.println("double output:" +d);
		System.out.println("float output:" +f);
		System.out.println("Long output: " +l);
		System.out.println("Int output:" +i);
		System.out.println("Short Output:" +s);
	System.out.println("Byte Output: " +b);
	System.out.println("Char Output: " +c);
	}
}

	
		